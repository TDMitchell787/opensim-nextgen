    fn map_type(&self, data_type: &DataType) -> String {
        match data_type {
            DataType::String => "string".to_string(),
            DataType::Integer => "int64".to_string(),
            DataType::Number => "float64".to_string(),
            DataType::Boolean => "bool".to_string(),
            DataType::Array(inner) => format!("[]{}", self.map_type(inner)),
            DataType::Object(name) => to_pascal_case(name),
            DataType::Optional(inner) => format!("*{}", self.map_type(inner)),
            DataType::Map(value_type) => format!("map[string]{}", self.map_type(value_type)),
        }
    }
