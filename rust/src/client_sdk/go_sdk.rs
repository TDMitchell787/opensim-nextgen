    fn generate_auth(&self, _schema: &ApiSchema) -> Result<String> {
        Ok(r#"package opensim

import (
	"context"
	"fmt"
)

// AuthConfig holds authentication configuration
type AuthConfig struct {
	Username    string `json:"username"`
	Password    string `json:"password"`
	Token       string `json:"token,omitempty"`
	ExpiresAt   int64  `json:"expires_at,omitempty"`
}

// LoginRequest represents a login request
type LoginRequest struct {
	Username string `json:"username"`
	Password string `json:"password"`
}

// LoginResponse represents a login response
type LoginResponse struct {
	Token     string `json:"token"`
	ExpiresAt int64  `json:"expires_at"`
	UserID    string `json:"user_id"`
}

// Login authenticates with the OpenSim API
func (c *Client) Login(ctx context.Context, username, password string) (*LoginResponse, error) {
	req := LoginRequest{
		Username: username,
		Password: password,
	}
	
	var resp Response[LoginResponse]
	err := c.makeRequest(ctx, "POST", "/api/auth/login", req, &resp)
	if err != nil {
		return nil, err
	}
	
	// Store auth config
	c.auth = &AuthConfig{
		Username:  username,
		Token:     resp.Data.Token,
		ExpiresAt: resp.Data.ExpiresAt,
	}
	
	return &resp.Data, nil
}

// Logout logs out from the OpenSim API
func (c *Client) Logout(ctx context.Context) error {
	if c.auth == nil {
		return fmt.Errorf("not logged in")
	}
	
	err := c.makeRequest(ctx, "POST", "/api/auth/logout", nil, nil)
	if err != nil {
		return err
	}
	
	c.auth = nil
	return nil
}

// IsAuthenticated checks if the client is authenticated
func (c *Client) IsAuthenticated() bool {
	return c.auth != nil && c.auth.Token != ""
}
"#.to_string())
    }

    fn generate_api_category(&self, schema: &ApiSchema, category: &str) -> Result<String> {
        let mut result = format!(r#"package opensim

import (
	"context"
	"fmt"
)

"#);

        let endpoints: Vec<_> = schema.endpoints.iter()
            .filter(|e| e.path.starts_with(&format!("/{}", category)))
            .collect();

        for endpoint in endpoints {
            result.push_str(&self.generate_endpoint_method(endpoint)?);
        }

        Ok(result)
    }

    fn generate_endpoint_method(&self, endpoint: &ApiEndpoint) -> Result<String> {
        let method_name = self.generate_method_name(&endpoint.path, &endpoint.method);
        let path_with_params = self.generate_path_with_params(&endpoint.path);
        
        let mut params = Vec::new();
        let mut path_params = Vec::new();
        
        // Add path parameters
        for param in &endpoint.parameters {
            if param.location == ParameterLocation::Path {
                params.push(format!("{} {}", to_camel_case(&param.name), self.map_type(&param.param_type)));
                path_params.push(param.name.clone());
            }
        }
        
        // Add query parameters as a struct
        if endpoint.parameters.iter().any(|p| p.location == ParameterLocation::Query) {
            params.push("opts QueryOptions".to_string());
        }
        
        // Add request body
        if let Some(request_body) = &endpoint.request_body {
            params.push(format!("req {}", self.map_type(&request_body.content_type)));
        }
        
        let params_str = if params.is_empty() {
            "ctx context.Context".to_string()
        } else {
            format!("ctx context.Context, {}", params.join(", "))
        };
        
        let return_type = if let Some(response) = endpoint.responses.get("200") {
            format!("(*{}, error)", self.map_type(&response.content_type))
        } else {
            "error".to_string()
        };

        Ok(format!(r#"// {} {}
func (c *Client) {}({}) {} {{
	path := "{}"
{}
	
	var result {}
	err := c.makeRequest(ctx, "{}", path, {}, &result)
	if err != nil {{
		return {}, err
	}}
	
	return {}, nil
}}

"#, 
            endpoint.method.to_uppercase(),
            endpoint.path,
            method_name,
            params_str,
            return_type,
            path_with_params,
            self.generate_path_replacements(&path_params),
            if let Some(response) = endpoint.responses.get("200") {
                self.map_type(&response.content_type)
            } else {
                "interface{}".to_string()
            },
            endpoint.method.to_uppercase(),
            if endpoint.request_body.is_some() { "req" } else { "nil" },
            if endpoint.responses.get("200").is_some() { "nil" } else { "" },
            if endpoint.responses.get("200").is_some() { "&result" } else { "nil" }
        ))
    }

    fn generate_method_name(&self, path: &str, method: &str) -> String {
        let parts: Vec<&str> = path.split('/').filter(|s| !s.is_empty() && !s.starts_with('{')).collect();
        let name = parts.join("_");
        to_pascal_case(&format!("{}_{}", method, name))
    }

    fn generate_path_with_params(&self, path: &str) -> String {
        path.replace('{', "%s").replace('}', "")
    }

    fn generate_path_replacements(&self, path_params: &[String]) -> String {
        if path_params.is_empty() {
            return String::new();
        }
        
        let replacements: Vec<String> = path_params.iter()
            .map(|p| to_camel_case(p))
            .collect();
        
        format!("\tpath = fmt.Sprintf(path, {})", replacements.join(", "))
    }

